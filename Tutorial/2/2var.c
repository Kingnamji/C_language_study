#include <stdio.h>

int main(void)
{
	int x = 10;
	int y = 20;
	printf("x = %d 입니다 .\n", x);
	printf("y = %d 입니다. \n", y);
	printf("x + y = %d 입니다. \n", x + y);
	printf("x - y = %d 입니다. \n", x - y);
	printf("x * y = %d 입니다. \n", x * y);
	printf("x / y = %d 입니다. \n", x / y); // 두개의 정수를 나누고 int형으로 출력하면 몫이 출력된다.

	return 0;
}